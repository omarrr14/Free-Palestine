// Update the current date display
function updateCurrentDate() {
  const date = new Date();
  const options = { year: 'numeric', month: 'long', day: 'numeric' };
  document.getElementById('fact-date').textContent = date.toLocaleDateString('en-US', options);
}

// Set a random background image
function setRandomBackground() {
  const randomIndex = Math.floor(Math.random() * backgroundImages.length);
  document.body.style.backgroundImage = `url('${backgroundImages[randomIndex]}')`;
}

// Load today's fact about Palestine
function loadTodaysFact() {
  const today = new Date();
  
  // Check if this is the first run
  if (!localStorage.getItem('factStartDate')) {
    // First run - save today's date and start with fact[0]
    localStorage.setItem('factStartDate', today.toISOString());
    localStorage.setItem('lastFactDate', today.toISOString());
    localStorage.setItem('currentFactIndex', '0');
    
    const fact = palestineFacts[0];
    document.getElementById('fact-title').textContent = fact.title;
    document.getElementById('fact-description').textContent = fact.description;
    return;
  }
  
  // Not the first run
  const lastFactDate = new Date(localStorage.getItem('lastFactDate'));
  let currentFactIndex = parseInt(localStorage.getItem('currentFactIndex'));
  
  // Check if we need to advance to the next fact
  if (today.toDateString() !== lastFactDate.toDateString()) {
    // It's a new day, show next fact
    currentFactIndex = (currentFactIndex + 1) % palestineFacts.length;
    localStorage.setItem('currentFactIndex', currentFactIndex.toString());
    localStorage.setItem('lastFactDate', today.toISOString());
  }
  
  const fact = palestineFacts[currentFactIndex];
  document.getElementById('fact-title').textContent = fact.title;
  document.getElementById('fact-description').textContent = fact.description;
}

// Initialize search functionality
function initSearch() {
  const searchForm = document.getElementById('search-form');
  const searchInput = document.getElementById('search-input');
  
  searchForm.addEventListener('submit', function(e) {
    e.preventDefault();
    const query = searchInput.value.trim();
    
    if (query) {
      const searchUrl = `https://www.google.com/search?q=${encodeURIComponent(query)}`;
      window.location.href = searchUrl;
    }
  });
}

// Initialize shortcuts
function initShortcuts() {
  // Load shortcuts from localStorage or use defaults
  let shortcuts = JSON.parse(localStorage.getItem('shortcuts'));
  
  // If shortcuts is null, undefined, or empty array, use defaults
  if (shortcuts === null || shortcuts === undefined) {
    shortcuts = [...defaultShortcuts]; // Use spread operator to clone the array
    localStorage.setItem('shortcuts', JSON.stringify(shortcuts));
  }
  
  // Clear the shortcuts container except for the add button
  const shortcutsContainer = document.getElementById('shortcuts-container');
  const addButton = document.getElementById('add-shortcut');
  shortcutsContainer.innerHTML = '';
  shortcutsContainer.appendChild(addButton);
  
  // Add each shortcut to the container
  shortcuts.forEach((shortcut, index) => {
    const shortcutElement = createShortcutElement(shortcut, index);
    shortcutsContainer.insertBefore(shortcutElement, addButton);
  });
}

// Create a shortcut element
function createShortcutElement(shortcut, index) {
  // Create container for the shortcut
  const shortcutContainer = document.createElement('div');
  shortcutContainer.className = 'shortcut-container';
  shortcutContainer.dataset.index = index;
  
  // Create the shortcut link
  const shortcutElement = document.createElement('a');
  shortcutElement.href = shortcut.url;
  shortcutElement.className = 'shortcut';
  
  // Create icon
  const iconElement = document.createElement('div');
  iconElement.className = 'shortcut-icon';
  iconElement.textContent = shortcut.name.charAt(0).toUpperCase();
  
  // Create name
  const nameElement = document.createElement('div');
  nameElement.className = 'shortcut-name';
  nameElement.textContent = shortcut.name;
  
  // Create delete button
  const deleteButton = document.createElement('button');
  deleteButton.className = 'shortcut-delete';
  deleteButton.innerHTML = '&times;'; // × character
  deleteButton.title = 'Delete shortcut';
  
  // Add event listener for delete button
  deleteButton.addEventListener('click', function(e) {
    e.preventDefault(); // Prevent navigating to the shortcut URL
    e.stopPropagation(); // Prevent event bubbling
    deleteShortcut(index);
  });
  
  // Assemble the shortcut
  shortcutElement.appendChild(iconElement);
  shortcutElement.appendChild(nameElement);
  
  shortcutContainer.appendChild(shortcutElement);
  shortcutContainer.appendChild(deleteButton);
  
  return shortcutContainer;
}

// Delete a shortcut
function deleteShortcut(index) {
  // Get current shortcuts
  let shortcuts = JSON.parse(localStorage.getItem('shortcuts')) || [];
  
  // Remove the shortcut at the specified index
  shortcuts.splice(index, 1);
  
  // Save updated shortcuts to localStorage
  localStorage.setItem('shortcuts', JSON.stringify(shortcuts));
  
  // Update the display
  initShortcuts();
}

// Initialize shortcut form functionality
function initShortcutForm() {
  const addButton = document.getElementById('add-shortcut');
  const shortcutForm = document.getElementById('shortcut-form');
  const overlay = document.getElementById('overlay');
  const cancelButton = document.getElementById('cancel-btn');
  const saveButton = document.getElementById('save-btn');
  const nameInput = document.getElementById('shortcut-name');
  const urlInput = document.getElementById('shortcut-url');
  
  // Open form when add button is clicked
  addButton.addEventListener('click', function(e) {
    e.preventDefault(); // Prevent default action if any
    shortcutForm.style.display = 'block';
    overlay.style.display = 'block';
    nameInput.value = '';
    urlInput.value = '';
    nameInput.focus();
  });
  
  // Close form when cancel button is clicked
  cancelButton.addEventListener('click', function() {
    shortcutForm.style.display = 'none';
    overlay.style.display = 'none';
  });
  
  // Close form when overlay is clicked
  overlay.addEventListener('click', function() {
    shortcutForm.style.display = 'none';
    overlay.style.display = 'none';
  });
  
  // Save shortcut when save button is clicked
  saveButton.addEventListener('click', function() {
    const name = nameInput.value.trim();
    let url = urlInput.value.trim();
    
    if (name && url) {
      // Add https:// if not present
      if (!url.startsWith('http://') && !url.startsWith('https://')) {
        url = 'https://' + url;
      }
      
      // Add new shortcut
      let shortcuts = JSON.parse(localStorage.getItem('shortcuts'));
      
      // Create array if it doesn't exist
      if (!shortcuts) {
        shortcuts = [];
      }
      
      shortcuts.push({ name, url });
      localStorage.setItem('shortcuts', JSON.stringify(shortcuts));
      
      // Update shortcuts display
      initShortcuts();
      
      // Close form
      shortcutForm.style.display = 'none';
      overlay.style.display = 'none';
    }
  });
  
  // Also allow form submission with Enter key
  shortcutForm.addEventListener('submit', function(e) {
    e.preventDefault();
    saveButton.click();
  });
}

